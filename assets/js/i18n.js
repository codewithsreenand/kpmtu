function getSavedLang() {
  const query = new URLSearchParams(window.location.search);
  const queryLang = query.get("lang");
  if (queryLang && (queryLang === "en" || queryLang === "ml")) {
    return queryLang;
  }
  const stored = window.localStorage.getItem(KPMTU_LANG_KEY);
  if (stored && (stored === "en" || stored === "ml")) return stored;
  return "en";
}

function setLang(lang) {
  if (!translations[lang]) return;
  window.localStorage.setItem(KPMTU_LANG_KEY, lang);
  document.documentElement.lang = lang;
  document.documentElement.setAttribute("lang", lang);
  document.documentElement.setAttribute("data-lang", lang);
  applyTranslations();
}

function t(key, vars = {}) {
  const lang = document.documentElement.lang || getSavedLang();
  const entry = translations[lang]?.[key] ?? translations.en[key] ?? key;
  return Object.keys(vars).reduce((text, varKey) => text.replace(`{${varKey}}`, vars[varKey]), entry);
}

function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (!key) return;
    const value = t(key, { year: new Date().getFullYear() });

    if (node.tagName === "INPUT" || node.tagName === "TEXTAREA") {
      if (node.placeholder) {
        node.placeholder = value;
      } else {
        node.value = value;
      }
    } else if (node.tagName === "OPTION") {
      node.textContent = value;
    } else {
      node.innerHTML = value;
    }
  });

  const langToggle = document.getElementById("lang-toggle");
  if (langToggle) {
    langToggle.textContent = t("lang_toggle");
  }

  document.dispatchEvent(new Event("kmptu:language-change"));
}

function setupLanguageToggle() {
  const toggle = document.getElementById("lang-toggle");
  if (!toggle) return;
  toggle.addEventListener("click", () => {
    const next = document.documentElement.lang === "en" ? "ml" : "en";
    setLang(next);
  });
}

function initI18n() {
  const lang = getSavedLang();
  document.documentElement.lang = lang;
  applyTranslations();
  setupLanguageToggle();
}

window.addEventListener("DOMContentLoaded", initI18n);
